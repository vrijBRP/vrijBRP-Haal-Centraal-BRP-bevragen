package nl.procura.haalcentraal.brp.bevragen.config.v1_3;

import javax.validation.constraints.NotNull;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;
import org.springframework.validation.annotation.Validated;

import lombok.Getter;
import lombok.Setter;

@Component
@ConfigurationProperties("procura.personrecordsource")
@Validated
@Getter
@Setter
public class PersonRecordSourceConfig {

  @NotNull
  private String url;

  private String username;

  private String password;



  public void setUrl(String url) {
    this.url = url.charAt(url.length() - 1) == '/' ? url.substring(0, url.length() - 1) : url;
  }

}
